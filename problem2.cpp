#include <stdio.h>

int main() {
    // Declare three integer variables to store the numbers
    int num1, num2, num3, sum;
    // Assign values to the three integer variables
    num1 = 10;
    num2 = 20;
    num3 = 30;
    // Calculate the sum of the three numbers
    sum = num1 + num2 + num3;
    printf("The sum of %d, %d, and %d is %d\n", num1, num2, num3, sum);
    return 0;
}
